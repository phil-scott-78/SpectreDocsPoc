using Spectre.Console;
using Spectre.Console.Rendering;

namespace Spectre.Docs.Examples.Showcase;

/// <summary>Demonstrates FigletText for ASCII art banners.</summary>
public class FigletSample : BaseSample
{
    /// <inheritdoc />
    public override void Run(IAnsiConsole console)
    {
        var dosRebel = FigletFont.Parse(DosRebel.Font);

        IRenderable[] outputs =
        [
            new FigletText("LEFT").LeftJustified().Color(Color.Blue),
            new FigletText("CENTER").Centered().Color(Color.Yellow),
            new FigletText("RIGHT").RightJustified().Color(Color.Green),
            new FigletText("STYLE").LeftJustified().Color(Color.Cyan1),
            new FigletText(dosRebel, "Custom").Centered().Color(Color.CornflowerBlue),
            new FigletText("HI!").Centered().Color(Color.Magenta1),
        ];

        // Animate
        console.Live(new Text("")).Start(context =>
        {
            foreach (var output in outputs)
            {
                context.UpdateTarget(output);
                context.Refresh();
                Thread.Sleep(2000);
            }
        });
    }
}

